#include "libft.h"

int	specd(int n)
{
	int	len;

	len = 0;
	if (n == -2147483648)
		return (specs("-2147483648"));
	if (n < 0)
	{
		len += specc('-');
		n *= -1;
	}
	if (n >= 10)
	{
		len += specd(n / 10);
		len += specd(n % 10);
	}
	else
		len += specc(n % 10 + '0');
	return (len);
}
