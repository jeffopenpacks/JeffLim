#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *str)
{
	char		*dest;
	int			i;
	size_t		len;

	len = ft_strlen(str);
	dest = malloc(sizeof(char) * (len + 1));
	if (!dest || !str)
		return (NULL);
	i = 0;
	while (str[i])
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
